#ifndef ALGORITMES_BUBBLE_SORT_H
#define ALGORITMES_BUBBLE_SORT_H

// bubbleSort() takes an array of integers and its size as parameters and sorts the array in ascending order using the Bubble Sort algorithm.
// The function iterates through the array, comparing each element to its adjacent element and swapping them if they are out of order. 
// This process is repeated until the entire array is sorted.
void bubbleSort(int arrForSort[], const int SIZE);

// printArray() takes an array of integers and its size as parameters and prints out each element of the array on a new line.
void printArray(int arr[], const int SIZE);

#endif //ALGORITMES_BUBBLE_SORT_H
